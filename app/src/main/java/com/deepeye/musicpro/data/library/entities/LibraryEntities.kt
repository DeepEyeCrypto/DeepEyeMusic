package com.deepeye.musicpro.data.library.entities

import androidx.room.Entity
import androidx.room.Index
import androidx.room.PrimaryKey
import com.deepeye.musicpro.domain.model.library.DownloadState

@Entity(tableName = "liked_tracks")
data class LikedTrackEntity(
    @PrimaryKey val videoId: String,
    val title: String,
    val artist: String,
    val channelId: String,
    val artworkUrl: String? = null,
    val likedAt: Long = System.currentTimeMillis(),
)

@Entity(tableName = "saved_tracks")
data class SavedTrackEntity(
    @PrimaryKey val videoId: String,
    val title: String,
    val artist: String,
    val channelId: String,
    val addedAt: Long = System.currentTimeMillis(),
)

@Entity(tableName = "playlists")
data class PlaylistEntity(
    @PrimaryKey val playlistId: String,
    val name: String,
    val description: String = "",
    val artworkUrl: String? = null,
    val isAutoGenerated: Boolean = false,
    val createdAt: Long = System.currentTimeMillis(),
    val updatedAt: Long = System.currentTimeMillis(),
)

@Entity(
    tableName = "playlist_tracks",
    primaryKeys = ["playlistId", "videoId"],
    indices = [Index("playlistId"), Index("videoId")],
)
data class PlaylistTrackEntity(
    val playlistId: String,
    val videoId: String,
    val title: String,
    val artist: String,
    val artworkUrl: String? = null,
    val position: Int,
    val addedAt: Long = System.currentTimeMillis(),
)

@Entity(tableName = "downloads")
data class DownloadEntity(
    @PrimaryKey val videoId: String,
    val title: String,
    val artist: String,
    val artworkUrl: String? = null,
    val localPath: String = "",
    val state: DownloadState = DownloadState.NONE,
    val progress: Int = 0,
    val sizeBytes: Long = 0L,
    val downloadedAt: Long = 0L,
    val updatedAt: Long = System.currentTimeMillis(),
)

@Entity(tableName = "recent_plays", indices = [Index("playedAt")])
data class RecentPlayEntity(
    @PrimaryKey(autoGenerate = true) val id: Long = 0,
    val videoId: String,
    val title: String,
    val artist: String,
    val artworkUrl: String? = null,
    val playedAt: Long = System.currentTimeMillis(),
)
